public class Calculate {
    public int sum(int a, int b) {
        return a + b;
    }

    public int div(int a, int b) {
        return a / b;
    }

    public float divFloat(float a, float b) {
        return a / b;
    }
    public float sqrt(float a){
        return (float) Math.sqrt(a);
    }
}
